<template>
  <div>
    <ConfigTable
      :addButton="true"
      :items.sync="roles"
      :headerFilter="['tag', 'id', 'name', 'description']"
      sortByItem="id"
      :actionColumn="true"
      @delete-item="deleteItem"
      @edit-item="editItem"
      @add-item="addItem"
    />
    <EditConfig
      v-if="formData && Object.keys(formData).length > 0"
      :configData="formData"
      :formFormat="formFormat"
      @submit="handleSubmit"
    ></EditConfig>
  </div>
</template>

<script>
import ConfigTable from '../../components/config/ConfigTable'
import EditConfig from '../../components/config/EditConfig'
import {
  deleteRoles,
  createRoles,
  updateRoles
} from '@/api/config'
import { mapActions, mapGetters } from 'vuex'
import { notifySuccess, emptyValues, notifyFailure } from '@/utils/helpers'

export default {
  name: 'Roles',
  components: {
    ConfigTable,
    EditConfig
  },
  data: () => ({
    roles: [],
    formData: {},
    edit: false,
    permissions: []
  }),
  computed: {
    formFormat() {
      return [
        {
          name: 'id',
          label: 'ID',
          type: 'text',
          disabled: true
        },
        {
          name: 'name',
          label: 'Name',
          type: 'text',
          required: true
        },
        {
          name: 'description',
          label: 'Description',
          type: 'textarea',
          required: true
        },
        {
          name: 'title',
          label: 'Title',
          type: 'text'
        },
        {
          name: 'subtitle',
          label: 'SubTitle',
          type: 'text'
        },
        {
          name: 'permissions',
          label: 'Permissions',
          type: 'table',
          headers: [
            { text: 'Name', value: 'name' },
            { text: 'Description', value: 'description' }
          ],
          items: this.permissions
        }
      ]
    }
  },
  methods: {
    ...mapActions('config', ['loadRoles', 'loadPermissions']),
    ...mapGetters('config', ['getRoles', 'getPermissions']),
    ...mapActions(['updateItemCount']),
    updateData() {
      this.loadRoles().then(() => {
        const sources = this.getRoles()
        this.roles = sources.items
        this.updateItemCount({
          total: sources.total_count,
          filtered: sources.length
        })
      })
      this.loadPermissions().then(() => {
        this.permissions = this.getPermissions().items
      })
    },
    addItem() {
      this.formData = emptyValues(this.roles[0])
      this.edit = false
    },
    editItem(item) {
      this.formData = item
      this.edit = true
    },
    handleSubmit(submittedData) {
      console.log(submittedData)
      if (this.edit) {
        this.updateItem(submittedData)
      } else {
        this.createItem(submittedData)
      }
    },
    deleteItem(item) {
      if (!item.default) {
        deleteRoles(item).then(() => {
          notifySuccess(`Successfully deleted ${item.name}`)
          this.updateData()
        }).catch(() => {
          notifyFailure(`Failed to delete ${item.name}`)
        })
      }
    },
    createItem(item) {
      createRoles(item).then(() => {
        notifySuccess(`Successfully created ${item.name}`)
        this.updateData()
      }).catch(() => {
        notifyFailure(`Failed to create ${item.name}`)
      })
    },
    updateItem(item) {
      updateRoles(item).then(() => {
        notifySuccess(`Successfully updated ${item.name}`)
        this.updateData()
      }).catch(() => {
        notifyFailure(`Failed to update ${item.name}`)
      })
    }
  },
  mounted() {
    this.updateData()
  },
  beforeDestroy() {}
}
</script>
